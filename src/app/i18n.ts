export type TranslationKey = keyof typeof dictionaries.en | string;
export type SupportedLang = 'en' | 'cs' | 'de';

export function getTranslation(lang: SupportedLang | string, section: string, key: string): string {
    const dictionary = dictionaries[lang as SupportedLang] || dictionaries.en;

    const resolvePath = (obj: any, path: string) => {
        return path.split('.').reduce((acc, part) => acc && acc[part], obj);
    };

    const sectionObj = resolvePath(dictionary, section);
    const translation = sectionObj?.[key];

    if (translation) return translation;

    const fallbackSectionObj = resolvePath(dictionaries.en, section);
    return fallbackSectionObj?.[key] || key;
}
